class MyChar {
    public static void main(String[] args) {
        char c = 'a';
        System.out.println("c: " + c);
        System.out.println("ASCII of c1: "+ (int)c);
        char c1 = (char)(c + 1);
        System.out.println("c1: " + c1);
        System.out.println("ASCII of c1: "+ (int)c1);
        
    }
}